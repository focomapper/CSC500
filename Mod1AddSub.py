# Get user input as float to handle decimals
number1 = float(input("Enter the first number: "))
number2 = float(input("Enter the second number: "))

# Add input
result = number1 + number2

# Print result
print(number1, "plus", number2, "=", result)

# Subtract input
result = number1 - number2

# Print result
print(number1, "minus", number2, "=", result)